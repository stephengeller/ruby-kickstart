# Iterate a hash, then iterate through the value of that hash

def pathify(paths=Hash.new)
  to_return = []
  arr = []
  starting_path = ["/"]

  #
  paths.each do |key, value|
    starting_path << key
    value.each do |key, value|
      arr << key << value
    end
  end


  to_return << arr.join('/')

  final_arr = []
  final_arr << starting_path.join << "/"  << to_return.join
  final_arr.join

  final_final_arr = []
  final_final_arr << final_arr.join
end

